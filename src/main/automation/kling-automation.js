/**
 * kling-automation.js — Phase 4 of the cinematic workflow.
 *
 * Generates multi-shot video clips via Higgsfield's Kling 3.0 model. Each
 * generation packs 2-4 dialogue lines into a single 10-12s clip with
 * cinematic cuts, native lip-synced audio, and Higgsfield element references
 * for character + voice + location identity lock.
 *
 * Architectural shift vs staged (Veo) pipeline:
 *   - Staged:    1 dialogue line   → 1 Veo clip (8s)
 *   - Cinematic: 1 kling_clips entry → 1 Kling clip (10-12s, contains 2-4 lines + cuts)
 *
 * The script's `scene.kling_clips` array (authored by Claude during Phase 1)
 * is the unit of work. Each entry has:
 *   - clip_id              — stable identifier, e.g. "ch3_sc3_c1"
 *   - duration_seconds     — 10-12s (capped at 12s to stay in Kling's reliable zone)
 *   - line_refs            — array of dialogue line numbers covered
 *   - multi_shot_prompt    — full Kling prompt with @element references + bracketed dialogue
 *
 * The start frame for a Kling generation comes from the corresponding
 * scene_image_cinematic generated by Phase 3 (Cinema Studio 2.0). All
 * kling_clips for the same scene share the same start frame.
 *
 * Validated end-to-end in Session 8 Phase 0 — Kling's official docs +
 * user's prior production use since January 2026 confirm:
 *   - Native audio with Nigerian English accent works
 *   - Multi-shot cuts land at prompted timings
 *   - Character consistency holds across cuts when start frame + element refs align
 *   - 10-12s is the safe duration zone (15s degrades)
 *
 * Automation gotchas (Phase 0 + Phase 4 inspection):
 *   1. Native file picker is unreachable from Playwright. Start frame upload
 *      MUST go through "Image Generations" tab in the picker dialog — never
 *      click the OS file dialog path.
 *   2. Multi-shot toggle defaults to OFF; click to enable, then Auto/Custom
 *      sub-buttons appear.
 *   3. Aspect ratio button disappears once a start frame is attached (Kling
 *      derives aspect from the start frame). Don't try to set it.
 *   4. Duration default is 15s; we explicitly set 10s for reliability.
 *   5. @ autocomplete: type 2-3 letter prefix, wait, Enter. Same pattern as
 *      Cinema Studio 2.0.
 *   6. Generation timing: 10s clip = ~90-120s. 15s clip = 2-3 min. Plan timeouts.
 *   7. Result downloads via cloudfront URL on the video element src.
 */

const path = require('path');

class KlingAutomation {
  /**
   * @param {Object} opts
   * @param {HiggsFieldAutomation} opts.automation - shared Playwright instance
   * @param {Function} [opts.logger]
   */
  constructor({ automation, logger }) {
    this.automation = automation;
    // Wrap logger to prepend HH:MM:SS timestamp for precise timing analysis
    const baselog = logger || ((msg) => console.log(`[KLING] ${msg}`));
    this.log = (msg, level) => {
      const now = new Date();
      const ts = now.toTimeString().slice(0, 8); // HH:MM:SS
      baselog(`[${ts}] ${msg}`, level);
    };
  }

  /**
   * Navigate to the Kling 3.0 Create Video page. Idempotent.
   *
   * UI flow (confirmed 2026-04-18):
   *   Top nav "Video" → "Kling 3.0" links to /kling-3
   *   which resolves to /ai/video with Kling 3.0 pre-selected.
   *   The page shows: preset banner (GENERAL / Kling 3.0), Start frame,
   *   Multi-shot toggle, prompt textarea, Elements, Generate button.
   *   No model switching needed — /kling-3 pre-selects the model.
   */
  async _ensureKling30Active() {
    const page = this.automation.page;
    if (!page) throw new Error('Playwright page not ready');

    // Navigate to the Kling 3.0 dedicated URL — this pre-selects the model.
    // Only navigate if we're not already on the right page.
    const currentUrl = page.url();
    if (!currentUrl.includes('/ai/video') && !currentUrl.includes('/kling-3')) {
      this.log('Navigating to Kling 3.0 page...');
      await page.goto('https://higgsfield.ai/kling-3', { waitUntil: 'domcontentloaded' });
      await page.waitForTimeout(3000); // give the SPA time to hydrate
    }

    // ── AD DISMISSAL: ads can appear 1-3s after page load and cover the UI ──
    await this._dismissAdsWithPatience('[KLING-NAV]');

    // Verify Kling 3.0 is shown on the page (model label in the sidebar)
    const kling30Visible = await page.getByText(/kling\s*3\.0/i).first()
      .isVisible({ timeout: 8000 }).catch(() => false);

    if (!kling30Visible) {
      // Retry: force-navigate to /kling-3 in case we landed on wrong model
      this.log('Kling 3.0 not visible — retrying navigation to /kling-3...');
      await page.goto('https://higgsfield.ai/kling-3', { waitUntil: 'domcontentloaded' });
      await page.waitForTimeout(3000);
      await this._dismissAdsWithPatience('[KLING-NAV-RETRY]');
      const retryVisible = await page.getByText(/kling\s*3\.0/i).first()
        .isVisible({ timeout: 5000 }).catch(() => false);
      if (!retryVisible) {
        throw new Error('Couldn\'t reach Kling 3.0 page (pre-gen setup failure): model label not visible after retry');
      }
    }
    this.log('Kling 3.0 page ready');
  }

  /**
   * Attach a previously-generated scene image as the start frame.
   *
   * UI (confirmed 2026-04-18): The Start frame slot is a <button type="file">
   * that triggers the OS native file picker. There is no in-page "Image
   * Generations" dialog in the current Higgsfield UI.
   *
   * Playwright approach: Use setInputFiles() on the hidden file input to
   * directly upload the image, bypassing the OS dialog entirely.
   *
   * @param {string} startFramePath - local path to the scene image
   * @param {string} [sourceGenId]  - unused (kept for API compat)
   */
  async _attachStartFrame(startFramePath, sourceGenId) {
    const fs = require('fs');
    const page = this.automation.page;

    // Verify the file exists before attempting upload
    if (!fs.existsSync(startFramePath)) {
      throw new Error(`Start frame file not found (pre-gen setup failure): ${startFramePath}`);
    }

    // Helper: find the start-frame thumbnail src in the DOM.
    // Returns the img.src string if a matching thumbnail exists, or null.
    const thumbFingerprint = `(() => {
      const imgs = document.querySelectorAll('img');
      for (const img of imgs) {
        const r = img.getBoundingClientRect();
        if (r.width > 40 && r.width < 250 && r.height > 40 && r.height < 250 && r.x < 350 && r.y < 400) {
          if (img.complete && img.naturalWidth > 0) {
            return img.src || img.currentSrc || null;
          }
        }
      }
      return null;
    })()`;

    // Snapshot the CURRENT thumbnail src BEFORE upload so we can detect when
    // the NEW image replaces it (prevents false-positive on old/placeholder thumbnails).
    const oldSrc = await page.evaluate(thumbFingerprint).catch(() => null);
    this.log(`Start frame pre-upload thumbnail: ${oldSrc ? oldSrc.slice(0, 80) + '...' : '(none)'}`);

    // The Start frame slot has a hidden <input type="file"> or <button type="file">.
    // Playwright's setInputFiles works on input[type=file] elements.
    // Find the file input associated with the Start frame area.
    try {
      // Strategy 1: Look for input[type="file"] elements near "Start frame"
      // The page has two file inputs — first one is Start frame, second is End frame.
      const fileInputs = await page.locator('input[type="file"], button[type="file"]').all();
      if (fileInputs.length === 0) {
        throw new Error('No file input elements found on page');
      }

      // Use the first file input (Start frame)
      const startFrameInput = fileInputs[0];
      await startFrameInput.setInputFiles(startFramePath);
      this.log(`Start frame uploaded via setInputFiles: ${path.basename(startFramePath)}`);
    } catch (e) {
      // Fallback: try clicking the Start frame area and handling the filechooser event
      this.log(`setInputFiles failed (${e.message.split('\n')[0]}) — trying filechooser fallback...`);
      try {
        const [fileChooser] = await Promise.all([
          page.waitForEvent('filechooser', { timeout: 5000 }),
          page.getByText('Start frame', { exact: false }).first().click({ timeout: 5000 }),
        ]);
        await fileChooser.setFiles(startFramePath);
        this.log(`Start frame uploaded via filechooser: ${path.basename(startFramePath)}`);
      } catch (fallbackErr) {
        throw new Error(`Couldn't attach start frame (pre-gen setup failure): ${fallbackErr.message}`);
      }
    }

    // Poll until the thumbnail src CHANGES (confirms upload + processing complete).
    // setInputFiles returns instantly but the UI needs time to process the image.
    let confirmed = false;
    for (let poll = 0; poll < 15; poll++) {
      await page.waitForTimeout(1000);
      const currentSrc = await page.evaluate(thumbFingerprint).catch(() => null);

      if (currentSrc && currentSrc !== oldSrc) {
        // Src changed — new image loaded successfully
        this.log(`Start frame thumbnail confirmed after ${poll + 1}s (src changed)`);
        confirmed = true;
        break;
      }
    }

    if (!confirmed) {
      throw new Error('[PRE-GEN] Start frame thumbnail not detected after 15s — upload may have failed');
    }

    // Hard 20s wait for Kling to finish processing the upload server-side.
    // The thumbnail src changes fast but the UI shows a spinner for ~15-18s.
    this.log('Start frame: waiting 20s for Kling to finish processing...');
    await page.waitForTimeout(20000);
    this.log('Start frame: ready');
  }

  /**
   * Toggle Multi-shot ON and select Auto mode.
   * Idempotent — safe to call when already in the desired state.
   */
  async _enableMultiShotAuto() {
    const page = this.automation.page;

    // DOM (confirmed via Chrome inspection 2026-04-18):
    //   <div>Multi-shot</div>
    //   <button role="switch" type="button" data-state="on|off" aria-checked="true|false">
    //     <span class="...translate-x-4..."/> (pill thumb)
    //   </button>
    // The switch has NO accessible name — getByRole('switch', {name:...}) won't work.
    // There is only ONE role="switch" on the Kling page.
    // When toggled ON: Auto/Custom buttons appear, data-state="on", aria-checked="true".

    // ── Step 1: Find the switch and check its current state ──
    const switchEl = page.locator('[role="switch"]').first();
    const switchVisible = await switchEl.isVisible({ timeout: 5000 }).catch(() => false);
    if (!switchVisible) {
      throw new Error('Multi-shot switch not found on page');
    }

    const currentState = await switchEl.getAttribute('data-state');
    const isOn = currentState === 'on' || (await switchEl.getAttribute('aria-checked')) === 'true';
    this.log(`Multi-shot current state: data-state="${currentState}", isOn=${isOn}`);

    // ── Step 2: Click to toggle ON if currently OFF ──
    if (!isOn) {
      await switchEl.click({ timeout: 3000 });
      await page.waitForTimeout(1000);

      // Verify it actually toggled
      const newState = await switchEl.getAttribute('data-state');
      if (newState !== 'on') {
        // Retry once
        this.log(`Multi-shot still ${newState} after click — retrying...`);
        await switchEl.click({ timeout: 3000 });
        await page.waitForTimeout(1000);
        const retryState = await switchEl.getAttribute('data-state');
        if (retryState !== 'on') {
          throw new Error(`Multi-shot toggle failed: state="${retryState}" after two clicks`);
        }
      }
    }

    // ── Step 3: Click Auto button (appears when Multi-shot is ON) ──
    const autoBtn = page.getByText('Auto', { exact: true }).first();
    const autoVisible = await autoBtn.isVisible({ timeout: 3000 }).catch(() => false);
    if (autoVisible) {
      await autoBtn.click({ timeout: 2000 });
      await page.waitForTimeout(300);
      this.log('Multi-shot ON, Auto mode selected');
    } else {
      // Auto might already be selected (default). Verify switch is still on.
      const finalState = await switchEl.getAttribute('data-state');
      if (finalState === 'on') {
        this.log('Multi-shot ON (Auto button not visible — may already be selected)');
      } else {
        throw new Error('Multi-shot toggle reverted — Auto button missing and state is off');
      }
    }
  }

  /**
   * Set duration on the Kling toolbar.
   *
   * Ensure the sound/audio toggle is ON.
   *
   * DOM (confirmed via Chrome inspection 2026-04-18):
   *   <input type="checkbox" name="enhancePrompt" checked> (sr-only)
   *   Wrapped in a <label> with a speaker icon SVG + "On"/"Off" text.
   *   Despite the name "enhancePrompt", this IS the sound toggle.
   *   Only visible when multi-shot is ON. When multi-shot OFF, the same
   *   spot shows the "Enhance" toggle instead.
   */
  async _ensureSoundOn() {
    const page = this.automation.page;

    const soundState = await page.evaluate(() => {
      const cb = document.querySelector('input[name="enhancePrompt"]');
      if (!cb) return { found: false };
      return { found: true, checked: cb.checked };
    });

    if (!soundState.found) {
      this.log('Warn: Sound checkbox (name="enhancePrompt") not found — may not be visible yet');
      return;
    }

    if (soundState.checked) {
      this.log('Sound already ON');
      return;
    }

    // Click the label to toggle it ON
    const label = page.locator('input[name="enhancePrompt"]').locator('..');
    await label.click({ timeout: 3000 });
    await page.waitForTimeout(500);

    // Verify
    const newState = await page.evaluate(() => {
      const cb = document.querySelector('input[name="enhancePrompt"]');
      return cb ? cb.checked : null;
    });
    if (newState) {
      this.log('Sound toggled ON');
    } else {
      throw new Error('Could not enable sound — checkbox still unchecked after click');
    }
  }

  /**
   * UI (confirmed 2026-04-18): A "Duration" button in the sidebar bottom row.
   * Clicking it opens an inline "Choose duration" panel with a text input
   * showing the current value (e.g. "15s"). We clear it and type the target.
   */
  async _setDuration(seconds) {
    const page = this.automation.page;
    const target = Math.max(3, Math.min(15, Math.round(seconds)));

    // DOM (confirmed via Chrome inspection 2026-04-18):
    //   Bottom bar has a chip button showing current duration (e.g. "15s").
    //   Clicking it opens a dialog containing:
    //     <input type="range" min="3" max="15" value="15" step="1">
    //   Setting the range input value and dispatching 'input'+'change' events
    //   updates the duration. Close dialog with Escape.

    try {
      // Step 1: Click the duration chip to open the dialog.
      // The chip is a button whose text is just digits + "s" (e.g. "5s", "10s", "15s").
      const durChip = page.locator('button').filter({ hasText: /^\d+s$/ }).first();
      await durChip.click({ timeout: 5000 });
      await page.waitForTimeout(500);

      // Step 2: Set the range input value via JavaScript (fill() + events).
      // Playwright's fill() doesn't work on range inputs — use evaluate instead.
      const rangeSet = await page.evaluate((val) => {
        const input = document.querySelector('input[type="range"]');
        if (!input) return false;
        // Set value via native setter to trigger React's synthetic events
        const nativeSetter = Object.getOwnPropertyDescriptor(
          window.HTMLInputElement.prototype, 'value'
        ).set;
        nativeSetter.call(input, val);
        input.dispatchEvent(new Event('input', { bubbles: true }));
        input.dispatchEvent(new Event('change', { bubbles: true }));
        return true;
      }, String(target));

      if (!rangeSet) {
        this.log('Warn: range input not found in duration dialog');
      }

      await page.waitForTimeout(300);

      // Step 3: Close the dialog
      await page.keyboard.press('Escape');
      await page.waitForTimeout(300);

      // Verify: the chip should now show the new value
      const chipText = await durChip.textContent().catch(() => '');
      this.log(`Duration set to ${target}s (chip shows: "${chipText.trim()}")`);
    } catch (e) {
      this.log(`Warn: duration set to ${target}s may have failed — ${e.message}`);
    }
  }

  /**
   * Type the multi_shot_prompt into Kling's prompt textbox. The prompt is
   * Claude-authored and contains @element references that need autocomplete-
   * triggered insertion (typing @claire as plain text won't bind it to the
   * element's visual + voice).
   *
   * IMPORTANT: Only CHARACTER @-references resolve to Higgsfield elements.
   * Location @-references (e.g. @mama_agbado_corn_stall) do NOT exist as
   * elements — they should be typed as plain text without the @ prefix.
   * The `validElements` set controls which @-references trigger autocomplete.
   *
   * Strategy: split the prompt on @-references, type each segment verbatim,
   * insert each @-reference via 3-letter prefix + Enter pattern (validated
   * in Phase 0).
   *
   * @param {string} promptText - The multi_shot_prompt to type
   * @param {Set<string>} [validElements] - Set of lowercase element names that
   *   exist in Higgsfield. Only @-references matching these will trigger
   *   autocomplete. Others are typed as plain text (without @).
   */
  async _typeMultiShotPrompt(promptText, validElements) {
    const page = this.automation.page;

    // DOM (confirmed via Chrome inspection 2026-04-18):
    //   <div role="textbox" contenteditable="true" class="...focus:outline-none size-full cursor-text p-3...">
    // It's a contenteditable div — no placeholder attribute, no <textarea>.
    // Use role="textbox" or contenteditable selector.
    const textbox = page.locator('[role="textbox"]').first();
    const tbVisible = await textbox.isVisible({ timeout: 5000 }).catch(() => false);
    if (!tbVisible) {
      throw new Error('Prompt textbox (role="textbox") not found on page');
    }

    await textbox.click();
    await page.waitForTimeout(200);
    // Clear any existing content
    await page.keyboard.press('Control+A');
    await page.keyboard.press('Delete');
    await page.waitForTimeout(200);

    // Parse the prompt: split on @word boundaries
    const segments = parsePromptSegments(promptText);

    for (const seg of segments) {
      if (typeof seg === 'string') {
        // Strip leading @ if it's standalone — we handle @-mentions via the
        // {at: name} segment type. Plain text segments shouldn't contain @
        // tokens (the parser separates them).
        await page.keyboard.type(seg);
      } else if (seg && seg.at) {
        const name = seg.at.toLowerCase().replace(/^@+/, '');
        // Only trigger @-autocomplete for valid character elements.
        // Location @-references are typed as plain text (no @ prefix)
        // since they don't exist as Higgsfield elements.
        if (validElements && !validElements.has(name)) {
          this.log(`Skipping @-autocomplete for "${name}" — not a valid element, typing as plain text`);
          await page.keyboard.type(name);
        } else {
          // Higgsfield's autocomplete only triggers when @ follows whitespace
          // or start-of-line. If the last typed char was e.g. '[' (from the
          // bracketed dialogue format [@character, speaking...]), the @
          // won't trigger autocomplete. Insert a space before @ if needed.
          const lastChar = await page.evaluate(() => {
            const tb = document.querySelector('[role="textbox"]');
            const text = tb ? (tb.innerText || tb.textContent || '') : '';
            return text.slice(-1);
          }).catch(() => '');
          if (lastChar && lastChar !== ' ' && lastChar !== '\n') {
            await page.keyboard.type(' ');
            await page.waitForTimeout(50);
          }

          // Type @ + full element name with per-character delay so the autocomplete
          // dropdown has time to render. Playwright's keyboard.type() fires
          // keystrokes too fast for Higgsfield's React autocomplete.
          // We type the FULL name (not just 3 chars) to avoid cross-project
          // element collisions where names share a prefix (e.g. adanna vs adaora).
          await page.keyboard.type('@', { delay: 0 });
          await page.waitForTimeout(400); // let @ trigger autocomplete listener
          await page.keyboard.type(name, { delay: 80 });

          // Wait for the autocomplete dropdown to appear (shows "Characters" header
          // with matching element names). The dropdown is a list rendered below
          // the textbox after a debounce (~300-500ms from last keystroke).
          await page.waitForTimeout(1500);

          // Check the dropdown and verify the first option matches our target element.
          // This prevents selecting a wrong element from a prior project that shares
          // a name prefix (e.g. @adanna_ms resolved instead of @adaora_hhb).
          const dropdownInfo = await page.evaluate(() => {
            const candidates = document.querySelectorAll('[class*="dropdown"], [class*="autocomplete"], [class*="mention"], [role="listbox"], [role="option"]');
            const options = document.querySelectorAll('[role="option"]');
            const visibleOptions = [...(options || [])].filter(o => o.getBoundingClientRect().width > 0);
            if (candidates.length > 0 || visibleOptions.length > 0) {
              return {
                found: true,
                optionCount: visibleOptions.length,
                options: visibleOptions.slice(0, 5).map(o => (o.textContent || '').trim().toLowerCase().slice(0, 50)),
              };
            }
            // Fallback: check for any new floating panel near the textbox
            const panels = document.querySelectorAll('div[class*="absolute"], div[class*="fixed"]');
            for (const p of panels) {
              const r = p.getBoundingClientRect();
              const text = (p.textContent || '').toLowerCase();
              if (r.height > 20 && r.height < 300 && text.includes('character')) {
                return { found: true, optionCount: 1, options: [text.slice(0, 50)] };
              }
            }
            return { found: false, optionCount: 0, options: [] };
          }).catch(() => ({ found: false, optionCount: 0, options: [] }));

          if (dropdownInfo.found) {
            // Verify the first option matches our target element
            const targetLower = name.toLowerCase();
            const firstOption = (dropdownInfo.options[0] || '');
            const matchesTarget = firstOption.includes(targetLower) || targetLower.includes(firstOption.replace(/[^a-z0-9_]/g, '').slice(0, targetLower.length));

            if (!matchesTarget && dropdownInfo.optionCount > 1) {
              // First option doesn't match — try to find the right one by arrowing down
              this.log(`@mention mismatch: wanted "${name}" but first option is "${firstOption}" — scanning dropdown`);
              let found = false;
              for (let i = 1; i < Math.min(dropdownInfo.optionCount, 5); i++) {
                const opt = dropdownInfo.options[i] || '';
                if (opt.includes(targetLower)) {
                  // Arrow down to this option
                  for (let a = 0; a < i; a++) {
                    await page.keyboard.press('ArrowDown');
                    await page.waitForTimeout(100);
                  }
                  found = true;
                  this.log(`@mention: found "${name}" at dropdown position ${i}`);
                  break;
                }
              }
              if (!found) {
                this.log(`WARN: @mention "${name}" not found in dropdown options: [${dropdownInfo.options.join(', ')}] — selecting first`);
              }
            }

            await page.keyboard.press('Enter');
            await page.waitForTimeout(300);
          } else {
            // Retry: wait longer and check again
            this.log(`Autocomplete dropdown not detected for "${name}" — waiting longer...`);
            await page.waitForTimeout(2000);
            await page.keyboard.press('Enter');
            await page.waitForTimeout(300);
          }
        }
      }
    }
  }

  /**
   * Click Generate, then poll for completion + download to outputPath.
   *
   * Polling strategy:
   *   - Wait for a new video tile to appear in the History panel
   *   - Extract the cloudfront video src
   *   - Fetch + write to outputPath
   *
   * Timeout: 6 minutes for 10s clips, 8 minutes for 15s clips.
   */
  async _generateAndDownload(outputPath, durationSeconds, onGenClicked) {
    const page = this.automation.page;
    const fs = require('fs');

    // Capture the most-recent video URL at start so we can detect a new one
    const initialFirstSrc = await page.evaluate(() => {
      const vids = document.querySelectorAll('video[src*="cloudfront"]');
      return vids[0]?.src || null;
    });

    // ── PRE-GEN VERIFICATION GATE ──
    // Hard gate: verify all critical settings before burning credits.
    // If any check fails, throw a [PRE-GEN] error to abort cleanly.
    //
    // DOM facts (confirmed via Chrome inspection 2026-04-18):
    //   Multi-shot: <button role="switch" data-state="on|off" aria-checked="true|false">
    //   Sound:      <input type="checkbox" name="enhancePrompt" checked> (misleading name — it's the sound toggle)
    //               Only visible when multi-shot is ON. Label shows speaker icon + "On"/"Off".
    //   Prompt:     <div role="textbox" contenteditable="true">
    //   Start frame: thumbnail <img> in left sidebar area (x<350, y<400)
    //   Note: "Enhance" toggle only appears when multi-shot is OFF — irrelevant for us.
    const preGenCheck = await page.evaluate(() => {
      const issues = [];

      // 1. Multi-shot must be ON
      const switchEl = document.querySelector('[role="switch"]');
      if (!switchEl) {
        issues.push('Multi-shot switch not found');
      } else {
        const state = switchEl.getAttribute('data-state');
        const checked = switchEl.getAttribute('aria-checked');
        if (state !== 'on' && checked !== 'true') {
          issues.push(`Multi-shot is OFF (data-state="${state}", aria-checked="${checked}")`);
        }
      }

      // 2. Sound must be ON (checkbox name="enhancePrompt", checked=true)
      const soundCheckbox = document.querySelector('input[name="enhancePrompt"]');
      if (!soundCheckbox) {
        issues.push('Sound checkbox not found');
      } else if (!soundCheckbox.checked) {
        issues.push('Sound is OFF');
      }

      // 3. Prompt must have content
      const textbox = document.querySelector('[role="textbox"]');
      const promptText = textbox ? (textbox.innerText || textbox.textContent || '').trim() : '';
      if (promptText.length < 20) {
        issues.push(`Prompt too short or empty (${promptText.length} chars)`);
      }

      // 4. Start frame must be attached (thumbnail image in left sidebar)
      const imgs = document.querySelectorAll('img');
      let hasStartFrame = false;
      for (const img of imgs) {
        const r = img.getBoundingClientRect();
        if (r.width > 40 && r.width < 300 && r.height > 40 && r.height < 300 && r.x < 350 && r.y < 400) {
          hasStartFrame = true;
          break;
        }
      }
      if (!hasStartFrame) {
        issues.push('Start frame not detected');
      }

      return {
        ok: issues.length === 0,
        issues,
        promptLength: promptText.length,
        promptPreview: promptText.slice(0, 80),
        soundOn: soundCheckbox ? soundCheckbox.checked : null,
      };
    });

    if (!preGenCheck.ok) {
      const msg = preGenCheck.issues.join('; ');
      throw new Error(`[PRE-GEN] Pre-generation check failed: ${msg}`);
    }
    this.log(`Pre-gen gate passed: prompt=${preGenCheck.promptLength} chars, multi-shot ON, sound=${preGenCheck.soundOn ? 'ON' : 'OFF'}, start frame OK`);

    // Click Generate — CREDITS BURNED FROM THIS POINT
    // Use coordinate-based Playwright click (real mouse events) to avoid
    // React swallowing DOM-level clicks. Verify generation actually started
    // before logging "credits burned".
    const genBtnBox = await page.evaluate(() => {
      const btns = document.querySelectorAll('button[type="submit"], button');
      for (const b of btns) {
        const text = b.textContent?.trim() || '';
        if (/generate/i.test(text) && b.getBoundingClientRect().width > 0) {
          const r = b.getBoundingClientRect();
          return { x: r.x + r.width / 2, y: r.y + r.height / 2, text };
        }
      }
      return null;
    });

    if (!genBtnBox) {
      throw new Error('GENERATE button not found on page');
    }
    // Parse credit cost from button text (e.g. "Generate19.25" → 19.25)
    let creditCost = null;
    if (genBtnBox.text) {
      const costMatch = genBtnBox.text.match(/([\d,.]+)\s*$/);
      if (costMatch) {
        creditCost = parseFloat(costMatch[1].replace(/,/g, ''));
        if (isNaN(creditCost)) creditCost = null;
      }
    }
    this.log(`Clicking GENERATE at (${Math.round(genBtnBox.x)}, ${Math.round(genBtnBox.y)}) — "${genBtnBox.text}" (cost: ${creditCost ?? 'unknown'})`);

    // ── SINGLE CLICK ONLY ──
    // CRITICAL: We click GENERATE exactly ONCE. The old 3-attempt escalation pattern
    // caused triple generation (3× "Generating" tiles) because didGenerationStart()
    // detection was unreliable — the CSS selectors didn't match the actual DOM,
    // causing all 3 attempts to fire. If the single click doesn't work, we throw
    // an error and let the orchestrator's retry loop handle it with a fresh browser context.
    await page.mouse.click(genBtnBox.x, genBtnBox.y);
    this.log('GENERATE clicked once — waiting for submission to register');
    await page.waitForTimeout(4000);

    this.log('Kling 3.0 generation submitted — credits burned');

    // Notify caller that Generate was clicked (for DB persistence + credit tracking)
    if (typeof onGenClicked === 'function') {
      try { onGenClicked(creditCost); } catch (_) {}
    }

    const maxWaitMs = durationSeconds >= 12 ? 8 * 60 * 1000 : 6 * 60 * 1000;
    const startedAt = Date.now();

    while (Date.now() - startedAt < maxWaitMs) {
      await page.waitForTimeout(3000);

      const currentFirstSrc = await page.evaluate(() => {
        const vids = document.querySelectorAll('video[src*="cloudfront"]');
        return vids[0]?.src || null;
      });

      if (currentFirstSrc && currentFirstSrc !== initialFirstSrc) {
        // New video appeared — download it
        try {
          const buffer = await page.evaluate(async (url) => {
            const res = await fetch(url);
            const blob = await res.blob();
            const arrayBuffer = await blob.arrayBuffer();
            return Array.from(new Uint8Array(arrayBuffer));
          }, currentFirstSrc);
          fs.writeFileSync(outputPath, Buffer.from(buffer));
          const sizeMb = (buffer.length / (1024 * 1024)).toFixed(2);
          this.log(`Downloaded Kling clip to ${outputPath} (${sizeMb} MB)`);
          return {
            path: outputPath,
            sourceGenId: this._extractGenId(currentFirstSrc),
            cdnUrl: currentFirstSrc,
            sizeBytes: buffer.length,
          };
        } catch (e) {
          throw new Error(`Download failed: ${e.message}`);
        }
      }
    }

    throw new Error(`Timeout waiting for Kling generation (${maxWaitMs / 1000}s)`);
  }

  _extractGenId(url) {
    // cloudfront URL format includes a UUID-like path component
    const match = url.match(/\/([a-f0-9-]{36})\//i);
    return match ? match[1] : null;
  }

  /**
   * Normalize a prompt string for fuzzy comparison. Strips whitespace runs,
   * lowercases, removes @-mention prefixes (Higgsfield renders them as badges
   * and the Copy button may or may not include the @ symbol).
   */
  _normalizePrompt(text) {
    return (text || '')
      .toLowerCase()
      .replace(/@/g, '')           // strip @ symbols
      .replace(/\s+/g, ' ')       // collapse whitespace
      .replace(/[""'']/g, '"')    // normalize quotes
      .trim();
  }

  /**
   * Compute similarity between two normalized prompt strings (0-100).
   * Uses longest common subsequence ratio — fast enough for <2500 char prompts.
   */
  _promptSimilarity(a, b) {
    const na = this._normalizePrompt(a);
    const nb = this._normalizePrompt(b);
    if (!na || !nb) return 0;
    if (na === nb) return 100;

    // For very long strings, use a cheaper character overlap check
    // (LCS on 2500+ chars is O(n²) — fine for one-off recovery)
    const shorter = na.length <= nb.length ? na : nb;
    const longer = na.length > nb.length ? na : nb;

    // Character bigram overlap (Dice coefficient)
    const bigrams = (str) => {
      const set = new Map();
      for (let i = 0; i < str.length - 1; i++) {
        const bg = str.slice(i, i + 2);
        set.set(bg, (set.get(bg) || 0) + 1);
      }
      return set;
    };
    const aBigrams = bigrams(shorter);
    const bBigrams = bigrams(longer);
    let intersection = 0;
    for (const [bg, count] of aBigrams) {
      intersection += Math.min(count, bBigrams.get(bg) || 0);
    }
    const totalBigrams = (shorter.length - 1) + (longer.length - 1);
    if (totalBigrams === 0) return 0;
    return Math.round((2 * intersection / totalBigrams) * 100);
  }

  /**
   * Recover a timed-out Kling video clip from the Higgsfield Asset library.
   *
   * Flow:
   *   1. Nuke context (page is in unknown state after timeout)
   *   2. Navigate to higgsfield.ai/asset/video (the Video assets grid)
   *   3. Scan video tiles in the date section matching gen timestamp (±1 day)
   *   4. Click each tile → click PROMPT "Copy" → read clipboard → compare
   *   5. On match (≥85% similarity): click Download → save → return success
   *
   * @param {string} submittedPrompt - the exact multi_shot_prompt submitted to Kling
   * @param {string} outputPath      - where to save the recovered mp4
   * @param {Object} [opts]
   * @param {number} [opts.minSimilarity=85] - minimum prompt similarity (0-100)
   * @param {number} [opts.maxTilesToCheck=8] - max tiles to click before giving up
   * @param {number} [opts.timeoutMs=60000]   - total recovery timeout
   * @returns {Promise<{path, sourceGenId, cdnUrl, assetUuid}|null>}
   */
  async recoverTimedOutClip(submittedPrompt, outputPath, opts = {}) {
    const fs = require('fs');
    const minSimilarity = opts.minSimilarity || 85;
    const maxTilesToCheck = opts.maxTilesToCheck || 8;
    const timeoutMs = opts.timeoutMs || 60000;
    const startedAt = Date.now();

    this.log('[RECOVERY] Starting Kling video recovery from Asset library...');

    // ── 1. Nuke context for clean navigation ──
    try {
      await this.automation.recreateContext();
    } catch (e) {
      this.log(`[RECOVERY] Context recreate failed: ${e.message}`);
      return null;
    }
    const page = this.automation.page;
    if (!page) {
      this.log('[RECOVERY] No page after context recreate');
      return null;
    }

    // ── 2. Navigate to the Video assets page ──
    try {
      await page.goto('https://higgsfield.ai/asset/video', {
        waitUntil: 'domcontentloaded',
        timeout: 20000,
      });
    } catch (navErr) {
      this.log(`[RECOVERY] Navigation failed: ${navErr.message.split('\n')[0]}`);
      return null;
    }
    await page.waitForTimeout(3000);

    // ── AD DISMISSAL: fresh page load may trigger promo overlays ──
    await this._dismissAdsWithPatience('[RECOVERY]');

    this.log(`[RECOVERY] Asset/video page loaded. URL: ${page.url()}`);

    // ── 3. Scan for video tiles ──
    // DOM structure (confirmed 2026-04-18):
    //   <figure data-asset-id="{uuid}" data-cell-id="{uuid}">
    //     <img data-asset-preview="{uuid}" src="...cloudfront..." />
    //   </figure>
    // No <a> tags — tiles are figures with data attributes. Single click
    // navigates to /asset/video/{uuid} (overlay detail view).
    let videoTiles = [];
    for (let poll = 0; poll < 4; poll++) {
      videoTiles = await page.evaluate(() => {
        const results = [];
        // Primary: figure elements with data-asset-id
        const figures = document.querySelectorAll('figure[data-asset-id]');
        for (const fig of figures) {
          const r = fig.getBoundingClientRect();
          if (r.width < 80 || r.height < 80 || r.width === 0) continue;
          const uuid = fig.getAttribute('data-asset-id') || null;
          results.push({
            uuid,
            x: Math.round(r.x + r.width / 2),
            y: Math.round(r.y + r.height / 2),
            href: uuid ? `/asset/video/${uuid}` : null,
          });
        }
        // Fallback: img elements with data-asset-preview
        if (results.length === 0) {
          const imgs = document.querySelectorAll('img[data-asset-preview]');
          for (const img of imgs) {
            const r = img.getBoundingClientRect();
            if (r.width < 80 || r.height < 80) continue;
            const uuid = img.getAttribute('data-asset-preview') || null;
            results.push({
              uuid,
              x: Math.round(r.x + r.width / 2),
              y: Math.round(r.y + r.height / 2),
              href: uuid ? `/asset/video/${uuid}` : null,
            });
          }
        }
        // Last resort: CDN images (no uuid available)
        if (results.length === 0) {
          const imgs = document.querySelectorAll('img[src*="cloudfront"], img[src*="higgs"]');
          for (const img of imgs) {
            const r = img.getBoundingClientRect();
            if (r.width < 80 || r.height < 80) continue;
            results.push({
              uuid: null,
              x: Math.round(r.x + r.width / 2),
              y: Math.round(r.y + r.height / 2),
              href: null,
            });
          }
        }
        return results;
      });
      if (videoTiles.length > 0) break;
      this.log(`[RECOVERY] No video tiles yet (poll ${poll + 1}/4) — waiting 2s...`);
      await page.waitForTimeout(2000);
    }

    if (videoTiles.length === 0) {
      this.log('[RECOVERY] No video tiles found on asset page — giving up');
      return null;
    }
    this.log(`[RECOVERY] Found ${videoTiles.length} video tile(s) — checking up to ${Math.min(maxTilesToCheck, videoTiles.length)}`);

    // ── 4. Click each tile, copy prompt, compare ──
    const tilesToCheck = videoTiles.slice(0, maxTilesToCheck);

    for (let i = 0; i < tilesToCheck.length; i++) {
      if (Date.now() - startedAt > timeoutMs) {
        this.log(`[RECOVERY] Timeout (${timeoutMs / 1000}s) — giving up after ${i} tiles`);
        return null;
      }

      const tile = tilesToCheck[i];
      this.log(`[RECOVERY] Checking tile ${i + 1}/${tilesToCheck.length} (uuid=${tile.uuid || 'unknown'})...`);

      try {
        // Open the tile's detail overlay.
        // DOM structure: <figure data-asset-id="{uuid}"> — single click
        // navigates to /asset/video/{uuid} (overlay on top of grid).
        // Primary: click figure coordinates (works natively).
        // Fallback: navigate directly to the detail URL.
        await page.mouse.click(tile.x, tile.y);
        await page.waitForTimeout(1500);
        // If click didn't navigate to detail, force via URL
        if (tile.uuid && !page.url().includes(tile.uuid)) {
          await page.goto(`https://higgsfield.ai/asset/video/${tile.uuid}`, {
            waitUntil: 'domcontentloaded', timeout: 15000,
          });
          await page.waitForTimeout(2000);
        }

        // Wait for the detail panel to load (PROMPT section + Copy button)
        let detailReady = false;
        for (let wait = 0; wait < 10; wait++) {
          detailReady = await page.evaluate(() => {
            const bodyText = document.body?.innerText || '';
            // Detail panel has PROMPT + Copy + INFORMATION sections
            return bodyText.includes('PROMPT') &&
                   (bodyText.includes('Copy') || bodyText.includes('INFORMATION'));
          }).catch(() => false);
          if (detailReady) break;
          await page.waitForTimeout(1000);
        }

        if (!detailReady) {
          this.log(`[RECOVERY] Detail panel didn't load for tile ${i + 1} — skipping`);
          // Close overlay (X or Escape) → returns to /asset/video grid
          await page.keyboard.press('Escape').catch(() => {});
          await page.waitForTimeout(1000);
          // If still on detail URL, force-navigate back
          if (page.url().match(/\/asset\/video\/[a-f0-9-]/i)) {
            await page.goto('https://higgsfield.ai/asset/video', { waitUntil: 'domcontentloaded', timeout: 15000 }).catch(() => {});
            await page.waitForTimeout(2000);
          }
          continue;
        }

        // Extract the asset UUID from the current URL
        const currentUrl = page.url();
        const assetUuidMatch = currentUrl.match(/\/asset\/video\/([a-f0-9-]{36})/i);
        const assetUuid = assetUuidMatch ? assetUuidMatch[1] : tile.uuid;

        // Click the Copy button next to PROMPT to get clean prompt text
        let copiedPrompt = '';
        try {
          // The Copy button is near the PROMPT heading. Find it.
          const copyBtn = await page.evaluate(() => {
            // Find all elements with text "Copy" near "PROMPT"
            const allBtns = [...document.querySelectorAll('button, [role="button"], span, div')];
            for (const el of allBtns) {
              const text = el.textContent?.trim();
              if (text === 'Copy' && el.getBoundingClientRect().width > 0) {
                // Verify it's near the PROMPT section (within 300px vertically)
                const r = el.getBoundingClientRect();
                const promptHeading = [...document.querySelectorAll('*')].find(e =>
                  e.textContent?.trim() === 'PROMPT' && e.getBoundingClientRect().width > 0
                );
                if (promptHeading) {
                  const pr = promptHeading.getBoundingClientRect();
                  if (Math.abs(r.y - pr.y) < 50) {
                    return { x: Math.round(r.x + r.width / 2), y: Math.round(r.y + r.height / 2) };
                  }
                }
              }
            }
            return null;
          });

          if (copyBtn) {
            // Clear clipboard, click Copy, read clipboard
            await page.evaluate(() => navigator.clipboard.writeText(''));
            await page.mouse.click(copyBtn.x, copyBtn.y);
            await page.waitForTimeout(500);

            copiedPrompt = await page.evaluate(() =>
              navigator.clipboard.readText()
            ).catch(() => '');
          }

          // Fallback: if clipboard didn't work, scrape the prompt text from the DOM
          if (!copiedPrompt || copiedPrompt.length < 20) {
            copiedPrompt = await page.evaluate(() => {
              // The prompt text is in a section after "PROMPT" heading, before "INFORMATION"
              const body = document.body?.innerText || '';
              const promptIdx = body.indexOf('PROMPT');
              const infoIdx = body.indexOf('INFORMATION');
              if (promptIdx >= 0 && infoIdx > promptIdx) {
                let section = body.substring(promptIdx + 6, infoIdx).trim();
                // Remove "Copy" and "See all" artifacts
                section = section.replace(/^Copy\s*/i, '').replace(/\s*See all\s*$/i, '').trim();
                return section;
              }
              return '';
            }).catch(() => '');
          }
        } catch (copyErr) {
          this.log(`[RECOVERY] Copy/scrape failed for tile ${i + 1}: ${copyErr.message.split('\n')[0]}`);
        }

        if (!copiedPrompt || copiedPrompt.length < 20) {
          this.log(`[RECOVERY] No prompt text for tile ${i + 1} — skipping`);
          await page.keyboard.press('Escape').catch(() => {});
          await page.waitForTimeout(1000);
          continue;
        }

        // Compare prompts
        const similarity = this._promptSimilarity(submittedPrompt, copiedPrompt);
        this.log(`[RECOVERY] Tile ${i + 1} similarity: ${similarity}% (need ≥${minSimilarity}%)`);
        this.log(`[RECOVERY]   Submitted (first 80): "${submittedPrompt.slice(0, 80)}..."`);
        this.log(`[RECOVERY]   Copied    (first 80): "${copiedPrompt.slice(0, 80)}..."`);

        if (similarity < minSimilarity) {
          // Not a match — close overlay and try next tile
          await page.keyboard.press('Escape').catch(() => {});
          await page.waitForTimeout(1000);
          continue;
        }

        // ── 5. MATCH FOUND — Download the video ──
        this.log(`[RECOVERY] ✓ MATCH at ${similarity}% — downloading ${assetUuid || 'unknown'}...`);

        try {
          const dlBtn = await page.getByText('Download', { exact: true }).first();
          const [download] = await Promise.all([
            page.waitForEvent('download', { timeout: 30000 }),
            dlBtn.click({ timeout: 5000 }),
          ]);
          await download.saveAs(outputPath);
          const stat = fs.statSync(outputPath);

          if (stat.size < 50000) {
            this.log(`[RECOVERY] Download too small (${stat.size} bytes) — likely a placeholder`);
            fs.unlinkSync(outputPath);
            await page.keyboard.press('Escape').catch(() => {});
            await page.waitForTimeout(1000);
            continue; // try next tile
          }

          const sizeMb = (stat.size / (1024 * 1024)).toFixed(2);
          this.log(`[RECOVERY] ✓ Downloaded to ${outputPath} (${sizeMb} MB)`);

          // Extract CDN URL from the video element if possible
          const cdnUrl = await page.evaluate(() => {
            const vid = document.querySelector('video[src*="cloudfront"], video source[src*="cloudfront"]');
            return vid?.src || vid?.getAttribute('src') || null;
          }).catch(() => null);

          await page.keyboard.press('Escape').catch(() => {});

          return {
            path: outputPath,
            sourceGenId: assetUuid || 'recovered',
            cdnUrl,
            assetUuid,
            similarity,
          };
        } catch (dlErr) {
          this.log(`[RECOVERY] Download failed: ${dlErr.message.split('\n')[0]}`);
          await page.keyboard.press('Escape').catch(() => {});
          return null;
        }
      } catch (tileErr) {
        this.log(`[RECOVERY] Error checking tile ${i + 1}: ${tileErr.message.split('\n')[0]}`);
        await page.keyboard.press('Escape').catch(() => {});
        await page.waitForTimeout(500);
        continue;
      }
    }

    this.log(`[RECOVERY] No matching video found after checking ${tilesToCheck.length} tiles`);
    return null;
  }

  /**
   * Generate a single Kling 3.0 multi-shot clip.
   *
   * Dismiss any promo/ad overlays using the shared HiggsFieldAutomation's
   * `_dismissPromoAd()`. Uses the same 3-round patience pattern as the
   * Cinema Studio flows: wait for ads to render, dismiss, wait again for
   * late-arriving ads, dismiss again.
   *
   * @param {string} tag - log prefix for tracing, e.g. '[KLING-NAV]'
   */
  async _dismissAdsWithPatience(tag) {
    try {
      const page = this.automation.page;
      if (!page) return;

      this.log(`${tag} Waiting 3s for any ads to render before dismissing...`);
      await page.waitForTimeout(3000);

      // Round 1
      await this.automation._dismissPromoAd();
      await page.waitForTimeout(1000);

      // Round 2: catch late-arriving ads
      await this.automation._dismissPromoAd();
      await page.waitForTimeout(1000);

      // Round 3: final sweep
      await this.automation._dismissPromoAd();

      this.log(`${tag} Ad dismissal complete, proceeding`);
    } catch (e) {
      this.log(`${tag} Ad dismissal failed (non-fatal): ${e.message.split('\n')[0]}`);
    }
  }

  /**
   * @param {Object} opts
   * @param {string} opts.startFramePath        - local path to the scene image (start frame)
   * @param {string} [opts.startFrameGenId]     - Higgsfield UUID for picker matching
   * @param {string} opts.multiShotPrompt       - Claude-authored prompt with @refs + bracketed dialogue
   * @param {number} [opts.durationSeconds=10]  - 4-15s, default 10s
   * @param {string} opts.outputPath            - where to save the downloaded mp4
   * @param {Set<string>} [opts.validElements]  - set of lowercase character element names
   *   that exist in Higgsfield. @-references not in this set are typed as plain text.
   * @returns {Promise<{path, sourceGenId, cdnUrl, sizeBytes, model}>}
   */
  async generateClip({ startFramePath, startFrameGenId, multiShotPrompt, durationSeconds = 10, outputPath, validElements, onGenClicked }) {
    if (!startFramePath) throw new Error('generateClip: startFramePath required');
    if (!multiShotPrompt) throw new Error('generateClip: multiShotPrompt required');
    if (multiShotPrompt.length > 2500) {
      throw new Error(`[PRE-GEN] Prompt exceeds 2500-char Kling limit (${multiShotPrompt.length} chars)`);
    }

    // ── PRE-GEN SETUP (no credits burned yet) ──
    // Any failure here is a setup issue, NOT a generation timeout.
    // Tag errors with [PRE-GEN] so the orchestrator knows not to attempt recovery.
    try {
      await this.automation.ensureBrowser();
      await this._ensureKling30Active();
      await this._attachStartFrame(startFramePath, startFrameGenId);
      await this._enableMultiShotAuto();
      await this._ensureSoundOn();
      await this._setDuration(durationSeconds);
      await this._typeMultiShotPrompt(multiShotPrompt, validElements);
      await this.automation.page.waitForTimeout(800);
    } catch (setupErr) {
      throw new Error(`[PRE-GEN] ${setupErr.message}`);
    }

    // ── GENERATION (credits burned after clicking Generate) ──
    // Timeouts here mean the video may have been submitted server-side.
    const result = await this._generateAndDownload(outputPath, durationSeconds, onGenClicked);
    return { ...result, model: 'kling-3.0' };
  }
}

/**
 * Parse a Claude-authored multi_shot_prompt into segments suitable for the
 * autocomplete-triggered typing pattern. Strings are typed verbatim;
 * {at: 'name'} objects trigger @autocomplete + Enter.
 *
 * Examples:
 *   "Shot 1: WIDE on @claire near table." →
 *     ["Shot 1: WIDE on ", {at: "claire"}, " near table."]
 *
 *   "[@claire, speaking]: \"hello\"" →
 *     ["[", {at: "claire"}, ", speaking]: \"hello\""]
 */
function parsePromptSegments(promptText) {
  const segments = [];
  const re = /@([a-z0-9_]+)/gi;
  let lastIdx = 0;
  let match;
  while ((match = re.exec(promptText)) !== null) {
    const before = promptText.slice(lastIdx, match.index);
    if (before) segments.push(before);
    segments.push({ at: match[1] });
    lastIdx = match.index + match[0].length;
  }
  const tail = promptText.slice(lastIdx);
  if (tail) segments.push(tail);
  return segments;
}

